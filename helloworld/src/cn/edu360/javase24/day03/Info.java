package cn.edu360.javase24.day03;

public class Info {
    String id;
    String name;
    int age;
    String sex;
    String phone;
    String addr;
    public Info(String id, String name, int age, String sex, String phone, String addr){
        this.id = id;
        this.name = name;
        this.age = age;
        this.sex = sex;
        this.phone = phone;
        this.addr = addr;
    }
}
